namespace nuilder;

public class BraveBoard 
{
    public string deck;
    public string tape;
    public string truck;
    public string wheel;
    

    public BraveBoard(string deck, string tape, string truck, string wheel)
    {
        this.deck = deck;
        this.tape = tape;
        this.truck = truck;
        this.wheel = wheel;
    }
    
}
